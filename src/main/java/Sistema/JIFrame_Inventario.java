    private void btnAdminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdminActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = jTable_Inventario.getSelectedRow();
        if (filaSeleccionada != -1) { 
            int id = (int) jTable_Inventario.getValueAt(filaSeleccionada, 0);
            String marca = jTable_Inventario.getValueAt(filaSeleccionada, 1).toString();
            String modelo = jTable_Inventario.getValueAt(filaSeleccionada, 2).toString();
            int ano = Integer.parseInt(jTable_Inventario.getValueAt(filaSeleccionada, 3).toString());
            String tipo = jTable_Inventario.getValueAt(filaSeleccionada, 4).toString();
            int existencia = Integer.parseInt(jTable_Inventario.getValueAt(filaSeleccionada, 5).toString());

            // Crear y mostrar el formulario de gestión
            JIFrame_Inventario_Gestor Fgestor = new JIFrame_Inventario_Gestor(this, nivelUsuario, id, marca, modelo,tipo, ano, existencia);
            desktopPane.add(Fgestor); 
            Fgestor.setVisible(true); 
        }
    }//GEN-LAST:event_btnAdminActionPerformed


