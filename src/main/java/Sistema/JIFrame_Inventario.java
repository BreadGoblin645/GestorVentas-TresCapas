    public void actualizarTablaInventario() {
        DefaultTableModel modelo = new DefaultTableModel();
            
        modelo.addColumn("ID");
        modelo.addColumn("Marca");
        modelo.addColumn("Modelo");
        modelo.addColumn("Año");
        modelo.addColumn("Tipo");
        modelo.addColumn("Existencia");
        
        try {
            ResultSet resultado = inventarioLogica.obtenerInventario();

            while (resultado.next()) {
                modelo.addRow(new Object[]{
                    resultado.getInt("id"),
                    resultado.getString("marca"),
                    resultado.getString("modelo"),
                    resultado.getInt("ano"),
                    resultado.getString("tipo"),
                    resultado.getInt("existencia")
                });
            }   
            jTable_Inventario.setModel(modelo);   
        } catch (Exception e) {
                System.out.println("Error al cargar los datos: " + e.getMessage());
        }
    }
    
    private void busquedaInventario(String criterio){
        DefaultTableModel modelo = new DefaultTableModel();
            
        modelo.addColumn("ID");
        modelo.addColumn("Marca");
        modelo.addColumn("Modelo");
        modelo.addColumn("Año");
        modelo.addColumn("Tipo");
        modelo.addColumn("Existencia");
        
        try {
            ResultSet resultado = inventarioLogica.buscarInventario(criterio);
            
            while (resultado.next()) {
                modelo.addRow(new Object[]{
                    resultado.getInt("id"),
                    resultado.getString("marca"),
                    resultado.getString("modelo"),
                    resultado.getInt("ano"),
                    resultado.getString("tipo"),
                    resultado.getInt("existencia")
                });
            }   
            jTable_Inventario.setModel(modelo);  
            
        } catch (Exception e) {
            System.out.println("Error al buscar datos: " + e.getMessage());
        }
        
        
    }
    
